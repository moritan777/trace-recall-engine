class ThreadedConceptMemoryStore:
    def __init__(self, db_path: str = DEFAULT_DB_PATH) -> None:
        self.db_path = db_path
        self.conn = sqlite3.connect(db_path)
        self.conn.row_factory = sqlite3.Row
        self.init_schema()

    def close(self) -> None:
        self.conn.close()

    def init_schema(self) -> None:
        cur = self.conn.cursor()

        cur.execute(
            """
            CREATE TABLE IF NOT EXISTS threads (
                thread_id TEXT PRIMARY KEY,
                date TEXT NOT NULL,
                source_text TEXT
            )
            """
        )

        cur.execute(
            """
            CREATE TABLE IF NOT EXISTS word_memories (
                word TEXT NOT NULL,
                thread_id TEXT NOT NULL,
                date TEXT NOT NULL,
                strength REAL NOT NULL,
                weight REAL NOT NULL,
                last_seen TEXT NOT NULL,
                seen_count INTEGER NOT NULL,
                PRIMARY KEY (word, thread_id),
                FOREIGN KEY (thread_id) REFERENCES threads(thread_id)
            )
            """
        )

        cur.execute(
            "CREATE INDEX IF NOT EXISTS idx_word_memories_word ON word_memories(word)"
        )
        cur.execute(
            "CREATE INDEX IF NOT EXISTS idx_word_memories_thread ON word_memories(thread_id)"
        )
        self.conn.commit()

    def create_thread(self, words: list[ExtractedWord], source_text: str | None) -> str:
        now = now_iso()
        date = now[:10]
        thread_id = "thread_" + uuid.uuid4().hex[:12]

        normalized: dict[str, float] = {}
        for item in words:
            word = normalize_word(item.word)
            if not word:
                continue
            weight = clamp(float(item.weight), 0.1, 2.0)
            if word in normalized:
                normalized[word] = max(normalized[word], weight)
            else:
                normalized[word] = weight

        cur = self.conn.cursor()
        cur.execute(
            "INSERT INTO threads(thread_id, date, source_text) VALUES (?, ?, ?)",
            (thread_id, date, source_text),
        )

        for word, weight in normalized.items():
            if "みつき" in normalized:
                # Probe用の最小補正。
                # 「みつき」が同じ発話内に含まれる場合のみ、ユーザー関連らしさとして少し重くする。
                weight *= 1.2
            cur.execute(
                """
                INSERT INTO word_memories(word, thread_id, date, strength, weight, last_seen, seen_count)
                VALUES (?, ?, ?, ?, ?, ?, ?)
                """,
                (word, thread_id, date, clamp(weight, 0.1, 2.4), clamp(weight, 0.1, 2.4), now, 1),
            )

        self.conn.commit()
        return thread_id

    def get_memories_by_word(self, word: str) -> list[WordMemory]:
        cur = self.conn.cursor()
        rows = cur.execute(
            """
            SELECT word, thread_id, date, strength, weight, last_seen, seen_count
            FROM word_memories
            WHERE word = ?
            """,
            (word,),
        ).fetchall()
        return [row_to_word_memory(r) for r in rows]

    def get_all_words(self) -> list[str]:
        rows = self.conn.execute(
            "SELECT DISTINCT word FROM word_memories ORDER BY word"
        ).fetchall()
        return [r["word"] for r in rows]

    def get_thread_words(self, thread_id: str) -> list[WordMemory]:
        rows = self.conn.execute(
            """
            SELECT word, thread_id, date, strength, weight, last_seen, seen_count
            FROM word_memories
            WHERE thread_id = ?
            ORDER BY weight DESC, word ASC
            """,
            (thread_id,),
        ).fetchall()
        return [row_to_word_memory(r) for r in rows]

    def get_threads_for_word(self, word: str) -> list[str]:
        rows = self.conn.execute(
            "SELECT thread_id FROM word_memories WHERE word = ?",
            (word,),
        ).fetchall()
        return [r["thread_id"] for r in rows]

    def get_thread(self, thread_id: str) -> Optional[Thread]:
        row = self.conn.execute(
            "SELECT thread_id, date, source_text FROM threads WHERE thread_id = ?",
            (thread_id,),
        ).fetchone()
        if row is None:
            return None
        words = [m.word for m in self.get_thread_words(thread_id)]
        return Thread(
            thread_id=row["thread_id"],
            date=row["date"],
            source_text=row["source_text"],
            words=words,
        )

    def reinforce_seen(
        self,
        activated_words: Iterable[ActivatedWord],
        reinforce_amount: float = DEFAULT_REINFORCE_AMOUNT,
        max_strength: float = DEFAULT_MAX_STRENGTH,
    ) -> None:
        now = now_iso()
        cur = self.conn.cursor()

        for aw in activated_words:
            for thread_id in aw.thread_ids:
                row = cur.execute(
                    """
                    SELECT strength, last_seen, seen_count
                    FROM word_memories
                    WHERE word = ? AND thread_id = ?
                    """,
                    (aw.word, thread_id),
                ).fetchone()
                if row is None:
                    continue

                days = days_between_iso(row["last_seen"], now)
                recency_bonus = 1.0 + math.exp(-days / 3.0)
                depth_penalty = DEPTH_DECAY.get(aw.best_depth, 0.05)
                increment = reinforce_amount * recency_bonus * depth_penalty
                new_strength = min(float(row["strength"]) + increment, max_strength)

                cur.execute(
                    """
                    UPDATE word_memories
                    SET strength = ?, last_seen = ?, seen_count = ?
                    WHERE word = ? AND thread_id = ?
                    """,
                    (
                        new_strength,
                        now,
                        int(row["seen_count"]) + 1,
                        aw.word,
                        thread_id,
                    ),
                )

        self.conn.commit()

    def list_threads(self, limit: int = 20) -> list[Thread]:
        rows = self.conn.execute(
            """
            SELECT thread_id, date, source_text
            FROM threads
            ORDER BY rowid DESC
            LIMIT ?
            """,
            (limit,),
        ).fetchall()
        result: list[Thread] = []
        for row in rows:
            result.append(
                Thread(
                    thread_id=row["thread_id"],
                    date=row["date"],
                    source_text=row["source_text"],
                    words=[m.word for m in self.get_thread_words(row["thread_id"])],
                )
            )
        return result


