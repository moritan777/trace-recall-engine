def cmd_chat(args: argparse.Namespace) -> int:
    store, extractor, engine, generator = build_components(args)
    try:
        print("Threaded Concept Memory Probe v0.4")
        print(f"LLM: {'enabled ' + normalize_llm_base_url(args.base_url) if normalize_llm_base_url(args.base_url) else 'disabled; fallback mode'}")
        print("HTTP proxy: bypassed for local/direct calls")
        print("Commands: /exit, /threads, /add <text>, /ask <text>")
        print("Default: input is treated as /ask, then stored as a new thread.")
        while True:
            try:
                text = input("\n> ").strip()
            except EOFError:
                print()
                break

            if not text:
                continue
            if text in {"/exit", "exit", "quit", "/quit"}:
                break
            if text == "/threads":
                for th in store.list_threads(20):
                    print(f"{th.thread_id} {th.date}: {' / '.join(th.words)}")
                continue

            mode = "ask_then_add"
            if text.startswith("/add "):
                mode = "add"
                text = text[5:].strip()
            elif text.startswith("/ask "):
                mode = "ask"
                text = text[5:].strip()

            words = extractor.extract(text)

            if mode in {"ask", "ask_then_add"}:
                result = engine.activate(words, top_words=args.top_words, top_threads=args.top_threads)
                print_activation(result)
                store.reinforce_seen(result.activated_words)
                print("\n[Response]")
                print(generator.generate(text, result))

            if mode in {"add", "ask_then_add"}:
                thread_id = store.create_thread(words, source_text=text)
                print(f"\n[saved] {thread_id}: " + " / ".join(w.word for w in words))

        return 0
    finally:
        store.close()


