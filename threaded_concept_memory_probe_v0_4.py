def cmd_config(args: argparse.Namespace) -> int:
    base_url = normalize_llm_base_url(args.base_url or args.chat_base_url or os.getenv("LLM_BASE_URL", ""))
    model = args.model or os.getenv("LLM_MODEL", "local-model")
