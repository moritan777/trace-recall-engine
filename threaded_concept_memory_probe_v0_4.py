def enable_localhost_proxy_bypass() -> None:
    """
    Force localhost / LAN-style local endpoints to bypass HTTP proxy settings.

    Some Windows environments have HTTP_PROXY / HTTPS_PROXY configured.
    urllib.request.urlopen may then try to send http://127.0.0.1:8080 through
    that proxy, which breaks local llama.cpp / proxy servers.

    This probe should always connect directly to localhost.
    """
    bypass_hosts = [
        "127.0.0.1",
        "localhost",
        "::1",
        "0.0.0.0",
    ]

    existing = os.environ.get("NO_PROXY") or os.environ.get("no_proxy") or ""
    parts = [p.strip() for p in existing.split(",") if p.strip()]
    for host in bypass_hosts:
        if host not in parts:
            parts.append(host)

    value = ",".join(parts)
    os.environ["NO_PROXY"] = value
    os.environ["no_proxy"] = value


def open_url_direct(req: urllib.request.Request, timeout: float):
    """
    Open URL without using environment proxy settings.
    """
    opener = urllib.request.build_opener(urllib.request.ProxyHandler({}))
    return opener.open(req, timeout=timeout)


