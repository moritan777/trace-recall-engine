def build_components(args: argparse.Namespace) -> tuple[ThreadedConceptMemoryStore, WordExtractor, ActivationEngine, ResponseGenerator]:
    store = ThreadedConceptMemoryStore(args.db)
    extractor = WordExtractor(
        base_url=args.base_url,
        api_key=args.api_key,
        model=args.model,
        timeout_sec=args.timeout,
    )
    engine = ActivationEngine(
        store=store,
        half_life_days=args.half_life_days,
        max_depth=args.max_depth,
    )
    generator = ResponseGenerator(
        base_url=args.base_url,
        api_key=args.api_key,
        model=args.model,
        timeout_sec=args.timeout,
    )
    return store, extractor, engine, generator


def cmd_add(args: argparse.Namespace) -> int:
    store, extractor, _, _ = build_components(args)
    try:
        words = extractor.extract(args.text)
        thread_id = store.create_thread(words, source_text=args.text)
        print(f"saved: {thread_id}")
        print("words:", " / ".join(f"{w.word}({w.weight:.2f})" for w in words))
        return 0
    finally:
        store.close()


def cmd_ask(args: argparse.Namespace) -> int:
    store, extractor, engine, generator = build_components(args)
    try:
        words = extractor.extract(args.text)
        result = engine.activate(words, top_words=args.top_words, top_threads=args.top_threads)
        print_activation(result)

        if not args.no_reinforce:
            store.reinforce_seen(result.activated_words)

        if not args.no_response:
            print("\n[Response]")
            print(generator.generate(args.text, result))
        return 0
    finally:
        store.close()


