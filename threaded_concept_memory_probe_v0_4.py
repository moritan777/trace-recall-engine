"""
Threaded Concept Memory Probe v0.4

This version introduces multi-hop activation.

Words are treated as primary memory nodes.

Threads are treated as traces of experiences connecting words.

Activation propagates:

Word
 -> Thread
 -> Word
 -> Thread
 -> Word

The goal is not document retrieval.

The goal is to verify whether natural associative recall can emerge from interconnected experience threads.

Meaning is never stored.

Meaning is generated only by the LLM from the activated concepts.

Usage examples:

  python threaded_concept_memory_probe_v0_4.py --base-url http://127.0.0.1:8080 --model local-model config
  python threaded_concept_memory_probe_v0_4.py --base-url http://127.0.0.1:8080 --model local-model seed-tests
  python threaded_concept_memory_probe_v0_4.py --base-url http://127.0.0.1:8080 --model local-model chat
"""

