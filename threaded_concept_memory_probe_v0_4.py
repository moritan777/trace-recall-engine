def normalize_llm_base_url(value: str | None) -> str:
    """
    Accept both:
      http://127.0.0.1:8080
      http://127.0.0.1:8080/v1

    Internally this script calls:
      {base_url}/chat/completions
    so the normalized base URL must end with /v1.
    """
    if not value:
        return ""
    url = value.strip().rstrip("/")
    if not url:
        return ""
    if url.endswith("/chat/completions"):
        url = url[: -len("/chat/completions")]
    if not url.endswith("/v1"):
        url += "/v1"
    return url


DEPTH_DECAY: dict[int, float] = {
    0: 1.00,
    1: 0.40,
    2: 0.15,
    3: 0.05,
}


