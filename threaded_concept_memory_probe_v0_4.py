    print(f"base_url: {base_url or '(not set; fallback mode)'}")
    print(f"model: {model}")
    print(f"api_key: {'set' if (args.api_key or os.getenv('LLM_API_KEY')) else '(not set)'}")
    print(f"NO_PROXY: {os.environ.get('NO_PROXY', '')}")
    print("proxy_mode: disabled for this script's HTTP calls")

    if not base_url:
        print("status: LLM disabled. Set --base-url or LLM_BASE_URL.")
        return 0

    ok, detail = check_openai_compatible_health(base_url, timeout_sec=min(args.timeout, 5.0))
    print("health:", "ok" if ok else "failed")
    print("detail:", detail)

    if not getattr(args, "generate_test", False):
        print("chat/completions generation test: skipped")
        print("Use config --generate-test only when the local model is already loaded and responsive.")
        return 0

    try:
        content = call_openai_compatible_chat(
            base_url=base_url,
            api_key=args.api_key or os.getenv("LLM_API_KEY", ""),
            model=model,
            messages=[
                {"role": "system", "content": "Return only JSON. No explanation."},
                {"role": "user", "content": "{\"ok\":true}"},
            ],
            temperature=0.0,
            max_tokens=16,
            timeout_sec=args.timeout,
        )
        print("generation: ok")
        print("sample:", content.strip()[:200])
    except TimeoutError as exc:
        print("generation: timeout")
        print("error:", exc)
    except Exception as exc:
        print("generation: failed")
        print("error:", exc)

    return 0


