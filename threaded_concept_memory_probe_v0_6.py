# This probe does not store sentences as memories.
# It stores unique words and links them to experience threads.
# Meaning, emotion, importance, and relationship interpretation
# must be generated by the LLM at runtime.

# このProbeは文章を記憶として保存しない。
# 単語は一意なノードとして保存し、経験を表すthreadへ多対多で紐づける。
# 重要性・感情・恋人らしさ・意味付けは保存しない。
# それらは現在入力と励起語群を受け取ったLLMが実行時に生成する。


DEFAULT_DB_PATH = "threaded_concept_memory_probe_v0_6.db"
DEFAULT_HALF_LIFE_DAYS = 14.0
DEFAULT_MAX_STRENGTH = 2.0
DEFAULT_REINFORCE_AMOUNT = 0.10
DEFAULT_MAX_DEPTH = 3

DEPTH_DECAY: dict[int, float] = {
    0: 1.00,
    1: 0.40,
    2: 0.15,
    3: 0.05,
    4: 0.02,
}


def enable_localhost_proxy_bypass() -> None:
    bypass_hosts = ["127.0.0.1", "localhost", "::1", "0.0.0.0"]
    existing = os.environ.get("NO_PROXY") or os.environ.get("no_proxy") or ""
    parts = [p.strip() for p in existing.split(",") if p.strip()]
    for host in bypass_hosts:
        if host not in parts:
            parts.append(host)
    value = ",".join(parts)
    os.environ["NO_PROXY"] = value
    os.environ["no_proxy"] = value


def open_url_direct(req: urllib.request.Request, timeout: float):
    opener = urllib.request.build_opener(urllib.request.ProxyHandler({}))
    return opener.open(req, timeout=timeout)


enable_localhost_proxy_bypass()


def normalize_llm_base_url(value: str | None) -> str:
    if not value:
        return ""
    url = value.strip().rstrip("/")
    if not url:
        return ""
    if url.endswith("/chat/completions"):
        url = url[: -len("/chat/completions")]
    if not url.endswith("/v1"):
        url += "/v1"
    return url


@dataclass
class WordNode:
    word_id: str
    word: str
    strength: float
    weight: float
    seen_count: int
    first_seen: str
    last_seen: str


@dataclass
class Thread:
    thread_id: str
    date: str
    source_text: str | None
    words: list[str]


@dataclass
class WordThreadLink:
    word_id: str
    word: str
    thread_id: str
    weight_in_thread: float
    added_at: str


@dataclass
class ExtractedWord:
    word: str
    weight: float


@dataclass
class ActivationTrace:
    from_type: str
    from_id: str
    to_type: str
    to_id: str
    depth: int
    score: float
    reason: str


@dataclass
class ActivatedWord:
    word: str
    score: float
    best_depth: int
    thread_ids: list[str]
    activated_by: list[str]


@dataclass
class ActivatedThread:
    thread_id: str
    score: float
    words: list[str]
    matched_words: list[str]
