def add_activated_by(target: dict[str, list[str]], key: str, value: str) -> None:
    target.setdefault(key, [])
    if value not in target[key]:
        target[key].append(value)


def add_word_activation(
    word_scores: dict[str, float],
    word_best_depth: dict[str, int],
    word_threads: dict[str, set[str]],
    word_activated_by: dict[str, list[str]],
    word: str,
    thread_ids: list[str],
    score: float,
    depth: int,
    activated_by: str,
) -> None:
    word_scores[word] = word_scores.get(word, 0.0) + score
    word_best_depth[word] = min(word_best_depth.get(word, 99), depth)
    word_threads.setdefault(word, set()).update(thread_ids)
    add_activated_by(word_activated_by, word, activated_by)


def estimate_prompt_tokens_rough(text: str) -> int:
    ascii_chars = sum(1 for c in text if ord(c) < 128)
    non_ascii_chars = len(text) - ascii_chars
    return int((ascii_chars / 4.0) + (non_ascii_chars / 1.6))


def prompt_stats(label: str, text: str) -> str:
    return f"{label}: chars={len(text)}, lines={text.count(chr(10)) + 1 if text else 0}, rough_tokens={estimate_prompt_tokens_rough(text)}"


def normalize_text(text: str) -> str:
    return text.strip().replace("　", " ")


def normalize_word(word: str) -> str:
    word = normalize_text(word)
    return word.strip(" \t\r\n。、,.!?！？「」『』（）()[]【】")


def clamp(v: float, lo: float, hi: float) -> float:
    return max(lo, min(hi, v))


def now_iso() -> str:
    return dt.datetime.now(dt.timezone.utc).isoformat(timespec="seconds")


def days_between_iso(start: str, end: str) -> float:
    try:
        s = dt.datetime.fromisoformat(start)
        e = dt.datetime.fromisoformat(end)
        return max((e - s).total_seconds() / 86400.0, 0.0)
    except Exception:
        return 0.0


