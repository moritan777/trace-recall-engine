def call_openai_compatible_chat(
    base_url: str,
    api_key: str,
    model: str,
    messages: list[dict[str, str]],
    temperature: float,
    max_tokens: int,
    timeout_sec: float,
) -> str:
    url = normalize_llm_base_url(base_url) + "/chat/completions"
    payload = {
        "model": model,
        "messages": messages,
        "temperature": temperature,
        "max_tokens": max_tokens,
        "stream": False,
    }
    headers = {"Content-Type": "application/json"}
    if api_key:
        headers["Authorization"] = f"Bearer {api_key}"

    req = urllib.request.Request(
        url=url,
        data=json.dumps(payload, ensure_ascii=False).encode("utf-8"),
        headers=headers,
        method="POST",
    )

    with open_url_direct(req, timeout=timeout_sec) as resp:
        raw = resp.read().decode("utf-8")

    data = json.loads(raw)
    return data["choices"][0]["message"]["content"]


def check_openai_compatible_health(base_url: str, timeout_sec: float = 3.0) -> tuple[bool, str]:
    base_url = normalize_llm_base_url(base_url)
    if not base_url:
        return False, "base_url is empty"

    root = base_url[:-3] if base_url.endswith("/v1") else base_url
    candidates = [root + "/health", base_url + "/models"]
    last_error = ""
    for url in candidates:
        try:
            req = urllib.request.Request(url=url, method="GET")
            with open_url_direct(req, timeout=timeout_sec) as resp:
                body = resp.read(200).decode("utf-8", errors="replace")
                return True, f"GET {url} -> HTTP {getattr(resp, 'status', 200)} {body[:120]}"
        except Exception as exc:
            last_error = f"GET {url} failed: {exc}"
    return False, last_error


def parse_json_object(text: str) -> dict[str, Any]:
    text = text.strip()
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        pass
    match = re.search(r"\{.*\}", text, flags=re.DOTALL)
    if not match:
        raise ValueError("No JSON object found in LLM output.")
    return json.loads(match.group(0))


def fallback_extract_words(text: str) -> list[ExtractedWord]:
    text = normalize_text(text)
    protected_terms = [
        *GENERIC_PERSON_TERMS, "チーズケーキ", "コーヒー", "紅茶", "好き",
        "カフェ", "帰り", "食べる", "映画", "ポップコーン",
        "カッターナイフ", "カッター", "怪我", "苦手", "昔",
        "誕生日", "記念日", "猫", "ペット", "ギター",
    ]

    found: list[ExtractedWord] = []
    for term in protected_terms:
        if term in text:
            found.append(ExtractedWord(term, 1.2 if len(term) >= 4 else 1.0))

    if "ケーキ" in text and "チーズケーキ" not in [w.word for w in found]:
        found.append(ExtractedWord("ケーキ", 1.0))
    if "カッター" in text and "カッターナイフ" not in [w.word for w in found]:
        found.append(ExtractedWord("カッターナイフ", 0.9))

    chunks = re.findall(r"[A-Za-z0-9]+|[ぁ-んァ-ン一-龥ー]{2,}", text)
    stop = {"です", "ます", "だった", "って", "これ", "それ", "今日", "明日", "昨日", "する", "した", "ある", "いる", "もの", "なもの"}
    for ch in chunks:
        ch = normalize_word(ch)
        if not ch or ch in stop:
            continue
        if len(ch) >= 8 and re.search(r"[はがをにへでとてもやの]", ch):
            continue
        if any(ch in term and ch != term for term in protected_terms):
            continue
        found.append(ExtractedWord(ch, 0.8))

    return dedupe_extracted_words(found)


def dedupe_extracted_words(words: list[ExtractedWord]) -> list[ExtractedWord]:
    d: dict[str, float] = {}
    for item in words:
        word = normalize_word(item.word)
        if not word:
            continue
        d[word] = max(d.get(word, 0.0), clamp(item.weight, 0.1, 2.0))
    return [ExtractedWord(w, weight) for w, weight in sorted(d.items(), key=lambda kv: kv[1], reverse=True)]


def match_known_words(input_word: str, known_words: list[str]) -> list[tuple[str, float]]:
    input_word = normalize_word(input_word)
    if not input_word:
        return []

    result: list[tuple[str, float]] = []
    for known in known_words:
        if input_word == known:
            result.append((known, 1.0))
        elif input_word in known or known in input_word:
            short_len = max(min(len(input_word), len(known)), 1)
            long_len = max(max(len(input_word), len(known)), 1)
            sim = 0.72 + 0.20 * (short_len / long_len)
            result.append((known, clamp(sim, 0.55, 0.92)))
        elif loose_katakana_match(input_word, known):
            result.append((known, 0.72))

    result.sort(key=lambda x: x[1], reverse=True)
    return result[:12]


def loose_katakana_match(a: str, b: str) -> bool:
    a2 = a.replace("ー", "")
    b2 = b.replace("ー", "")
    return len(a2) >= 3 and len(b2) >= 3 and (a2 in b2 or b2 in a2)


