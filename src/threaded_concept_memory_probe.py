def format_recall_state_view(gated: GatedContext, origin_order: str = "none") -> list[str]:
    lines: list[str] = ["", "[Recall State]"]
    ordered = ordered_thread_groups(gated.threads, origin_order)
    if normalize_origin_order(origin_order) == "group":
        current_origin = None
        for th in ordered:
            origin = th.dominant_created_by or "user"
            if origin != current_origin:
                lines.append("")
                lines.append(f"{origin}-origin traces:")
                current_origin = origin
            words = " / ".join(th.core_words or th.words)
            direct = " / ".join(th.direct_words) if th.direct_words else "-"
            lines.append(f"- {words}")
            lines.append(f"  occurrence={th.occurrence_count}")
            lines.append(f"  origin={format_origin_counts(th.created_by_counts, th.dominant_created_by)}")
            lines.append(f"  score={th.group_score:.2f}")
            lines.append(f"  direct={direct}")
        return lines
    for th in ordered:
        words = " / ".join(th.core_words or th.words)
        direct = " / ".join(th.direct_words) if th.direct_words else "-"
        lines.append(f"- {words}")
        lines.append(f"  occurrence={th.occurrence_count}")
        lines.append(f"  origin={format_origin_counts(th.created_by_counts, th.dominant_created_by)}")
        lines.append(f"  score={th.group_score:.2f}")
        lines.append(f"  direct={direct}")
    return lines


def format_edge_view(gated: GatedContext) -> list[str]:
    edge_counts: dict[tuple[str, str], int] = {}
    edge_scores: dict[tuple[str, str], float] = {}
    for th in gated.threads:
        words = th.core_words or th.words
        for left, right in zip(words, words[1:]):
            key = (left, right)
            edge_counts[key] = edge_counts.get(key, 0) + th.occurrence_count
            edge_scores[key] = edge_scores.get(key, 0.0) + th.group_score

    lines: list[str] = ["", "[Edge Activation]"]
    for (left, right), count in sorted(edge_counts.items(), key=lambda item: (edge_scores[item[0]], item[1]), reverse=True):
        lines.append(f"- {left} -- {right} count={count}")
    return lines


def build_response_prompt(user_input: str, activation: ActivationResult, include_trace: bool = False, trace_limit: int = 10, gated: Optional[GatedContext] = None, prompt_view: str = "threadgroup", origin_order: str = "none") -> str:
    lines: list[str] = []
    lines.append("[Current Input]")
    lines.append(user_input)

    lines.append("")
    lines.append("[Extracted Input Words]")
    for w in activation.input_words:
        lines.append(f"- {w.word} ({w.weight:.2f})")
    lines.append("@user and @assistant are conversational participant references.")
    lines.append("They indicate who first-person or second-person expressions referred to in the original utterance.")

    if gated is not None:
        lines.extend(format_gated_recall_context(gated, prompt_view, origin_order))
    else:
        lines.append("")
        lines.append("[Activated Words]")
        for aw in activation.activated_words:
            by = ", ".join(aw.activated_by[:3])
            threads = ", ".join(aw.thread_ids[:5])
            lines.append(f"- {aw.word} score={aw.score:.3f} depth={aw.best_depth} activated_by={by} threads={threads}")

        lines.append("")
        lines.append("[Activated Experience Threads]")
        lines.append("These are not quoted memories. They are experience threads activated by overlapping words.")
        for th in activation.activated_threads:
            words = " / ".join(th.words)
            matched = ", ".join(th.matched_words)
            direct_hint = ""
            if th.common_bonus > 0:
                direct_hint = f" common_bonus={th.common_bonus:.3f}"
            lines.append(
                f"{th.thread_id} origin={th.created_by} score={th.score:.3f} base={th.base_score:.3f}{direct_hint} matched={matched}: {words}"
            )

    if include_trace:
        lines.append("")
        lines.append("[Activation Trace]")
        for tr in activation.traces[:trace_limit]:
            lines.append(f"- d{tr.depth}: {tr.from_type}:{tr.from_id} -> {tr.to_type}:{tr.to_id} score={tr.score:.3f}")
    else:
        lines.append("")
        lines.append("[Activation Trace]")
        lines.append("(omitted from LLM prompt; visible in console log only)")

    lines.append("")
    lines.append("[Instruction]")
    lines.append("励起された単語とthreadは正確な文章記憶ではなく連想の手がかりである。")
    lines.append("複数の入力語が同じthreadを指している場合、そのthreadをより強い手がかりとして扱う。")
    lines.append("Gated Recall Context がある場合は、それを優先し、Suppressed Words は返答の主材料にしない。")
    lines.append("回答では、必要なら『思い浮かんだ』『つながって出てきた』のように自然な想起として表現する。")
    lines.append("断定しすぎず、自然な想起として短く返す。")
    return "\n".join(lines)


