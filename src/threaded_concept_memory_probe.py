# This probe does not store sentences as memories.
# It stores unique words and links them to experience threads.
# Meaning, emotion, importance, and relationship interpretation
# must be generated by the LLM at runtime.

# このProbeは文章を記憶として保存しない。
# 単語は一意なノードとして保存し、経験を表すthreadへ多対多で紐づける。
# 重要性・感情・恋人らしさ・意味付けは保存しない。
# それらは現在入力と励起語群を受け取ったLLMが実行時に生成する。

# Recall strength and conversation exposure are separate.
# A trace can be easy to recall but still be suppressed from conversation
# if it has already been exposed too often recently.

# 思い出しやすさと、会話で出すべきかは別である。
# 強く励起したTraceでも、直近で何度も会話に出していれば抑制する。
# これは忘却ではなく、しつこさを避けるための会話制御である。


