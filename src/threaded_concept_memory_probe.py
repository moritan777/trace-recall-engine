"""
Threaded Concept Memory Probe v0.9.2

Unique Word Node + WordThread Link Table.

This version changes the memory model:

Word is unique.
Thread is an experience.
Word and Thread are connected by many-to-many links.

Activation propagates:

Word
 -> Thread
 -> Word
 -> Thread
 -> Word

A thread becomes stronger when multiple activated words point to the same thread.
This is the core experiment:

"交差する糸が強く震える"

v0.7 adds:
- common activation bonus for threads supported by multiple input words
- mutual amplification between words co-activated through the same strong thread
- response prompt grouping by strongest experience threads

v0.8 adds:
- Activation Gate before prompt construction
- raw activation can spread widely, but prompt candidates are narrowed
- direct input intersections are preferred
- depth2+ words are treated as supporting candidates

The goal is not document retrieval.
The goal is to verify whether natural associative recall can emerge from
interconnected experience threads.

Meaning is never stored.
Meaning is generated only by the LLM from activated concepts.
"""

