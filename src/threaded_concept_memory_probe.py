def research_log_markdown(record: dict[str, Any]) -> str:
    def block(value: Any) -> str:
        if isinstance(value, str):
            text = value
        elif isinstance(value, list):
            text = "\n".join(str(v) for v in value)
        else:
            text = json.dumps(value, ensure_ascii=False, indent=2)
        return f"```text\n{text}\n```"
    ev = record.get("evaluation", {})
    wm = record.get("working_memory", {})
    timing = record.get("timing", {})
    lines = [
        "# Research Log", "",
        f"- Experiment: {record.get('experiment_name','')}",
        f"- Experiment Type: {record.get('experiment_type','')}",
        f"- Dimension: {record.get('dimension','')}",
        f"- Comparison Value: {record.get('comparison_value','')}",
        f"- Conversation File: {record.get('conversation_file','')}",
        f"- Turn: {record.get('turn','')}",
        f"- Mode: {record.get('mode','')}",
        f"- Role: {record.get('role','')}",
        f"- Prompt View: {wm.get('prompt_view','')}",
        f"- Participant Reference: {wm.get('participant_reference','')}",
        f"- Origin Order: {wm.get('origin_order','')}", "",
        "## Input", "", block(record.get("input_text", "")), "",
        "## Extracted Words", "", block(record.get("extractor", {}).get("raw_words", [])), "",
        "## Normalized Words", "", block(record.get("extractor", {}).get("normalized_words", [])), "",
        "## Selected Thread Groups", "", block(record.get("recall", {}).get("selected_thread_groups", [])), "",
        "## Selected Words", "", block(record.get("recall", {}).get("selected_words", [])), "",
        "## Working Memory", "", block(wm), "",
        "## Final Prompt", "", block(record.get("prompt", {}).get("text", "")), "",
        "## LLM Response", "", block(record.get("response", {}).get("text", "")), "",
        "## Evaluation", "",
        "| Metric | Value |", "|---|---:|",
        f"| Expected hits | {ev.get('expected_hit_count', 0)} |",
        f"| Unexpected hits | {ev.get('unexpected_hit_count', 0)} |",
        f"| Response used expected | {ev.get('response_used_expected_count', 0)} |",
        f"| Response used unexpected | {ev.get('response_used_unexpected_count', 0)} |",
        f"| Precision-like | {float(ev.get('precision_like', 0.0)):.3f} |",
        f"| Prompt rough tokens | {record.get('prompt', {}).get('rough_tokens', 0)} |",
        f"| Recall time ms | {float(timing.get('recall_ms', 0.0)):.1f} |",
        f"| LLM response time ms | {float(timing.get('llm_response_ms', 0.0)):.1f} |", "",
        "## Human Notes", "", "<!-- Add interpretation here. -->", "",
    ]
    return "\n".join(lines)


def write_research_logs(jsonl_path: Path | None, md_dir: Path | None, records: list[dict[str, Any]]) -> None:
    try:
        if jsonl_path is not None:
            write_jsonl(jsonl_path, records)
        if md_dir is not None:
            md_dir.mkdir(parents=True, exist_ok=True)
            for record in records:
                turn = int(record.get("turn") or 0)
                prefix = f"{record.get('comparison_value')}_" if record.get("comparison_value") else ""
                (md_dir / f"{prefix}turn_{turn:04d}.md").write_text(research_log_markdown(record), encoding="utf-8")
    except Exception as exc:
        print(f"[warn] Research Logger write failed: {exc}", file=sys.stderr)

def evaluate_recall_turn(
    turn: int,
    mode: str,
    expected_words: list[str],
    unexpected_words: list[str],
    activation: ActivationResult,
    gated: GatedContext,
    prompt: str,
    response_text: str,
    response_skipped: bool = False,
) -> dict[str, Any]:
    gated_word_set = {gw.word for gw in gated.words}
    working_memory_group_count = len(gated.threads)
    working_memory_word_count = len(gated_word_set)
    response_used_threads = [th for th in gated.threads if response_uses_thread_group(response_text, th)]
    response_used_groups = [th.canonical_key for th in response_used_threads]
    response_expected = [w for w in expected_words if w and w in response_text]
    response_unexpected = [w for w in unexpected_words if w and w in response_text]
    expected_hits = [w for w in expected_words if w in gated_word_set or (w and w in response_text)]
    unexpected_hits = [w for w in unexpected_words if w in gated_word_set or (w and w in response_text)]
    prompt_only = [w for w in gated_word_set if w and w not in response_text]
    denom = len(expected_hits) + len(unexpected_hits)
    precision_like = (len(expected_hits) / denom) if denom else (1.0 if not unexpected_hits else 0.0)
    return {
        "turn": turn,
        "mode": mode,
        "input_words": [w.word for w in activation.input_words],
        "activated_words": [aw.word for aw in activation.activated_words],
        "gated_words": sorted(gated_word_set),
        "selected_thread_groups": [th.canonical_key for th in gated.threads],
        "suppressed_words": [sw.word for sw in gated.suppressed_words],
        "prompt_tokens_rough": prompt_stats_dict(prompt)["rough_tokens"],
        "prompt_thread_group_count": working_memory_group_count,
        "prompt_word_count": working_memory_word_count,
        "response_text": response_text,
        "response_skipped": response_skipped,
        "expected_hit_count": len(expected_hits),
        "unexpected_hit_count": len(unexpected_hits),
        "recall_precision_like": precision_like,
        "recall_noise_count": len(unexpected_hits),
        "fatigue_suppressed_count": sum(1 for sw in gated.suppressed_words if sw.suppressed_by_fatigue),
        "response_used_expected_count": len(response_expected),
        "response_used_unexpected_count": len(response_unexpected),
        "working_memory_group_count": working_memory_group_count,
        "working_memory_user_origin_group_count": sum(1 for th in gated.threads if th.dominant_created_by == "user"),
        "working_memory_assistant_origin_group_count": sum(1 for th in gated.threads if th.dominant_created_by == "assistant"),
        "response_used_group_count": len(response_used_groups),
        "response_used_user_origin_group_count": sum(1 for th in response_used_threads if th.dominant_created_by == "user"),
        "response_used_assistant_origin_group_count": sum(1 for th in response_used_threads if th.dominant_created_by == "assistant"),
        "origin_mixed_group_count": sum(1 for th in gated.threads if len(th.created_by_counts) > 1),
        "recall_efficiency": (len(response_used_groups) / working_memory_group_count) if working_memory_group_count else 0.0,
        "working_memory_word_count": working_memory_word_count,
        "response_used_groups": response_used_groups,
        "expected_hits": expected_hits,
        "unexpected_hits": unexpected_hits,
        "prompt_only": sorted(prompt_only),
        "prompt_only_words": sorted(prompt_only),
    }


def response_uses_thread_group(response_text: str, group: GatedThreadGroup) -> bool:
    if not response_text:
        return False
    candidate_words = merge_unique(group.core_words + group.direct_words + group.support_words + group.words)
    return any(word and word in response_text for word in candidate_words)


